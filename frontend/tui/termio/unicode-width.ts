export function stringWidth(str: string): number {
  let width = 0

  for (const char of str) {
    const code = char.codePointAt(0) ?? 0

    if (code === 0) continue
    if (code < 32 || (code >= 0x7f && code < 0xa0)) continue
    if (isCombiningCharacter(code)) continue

    if (isWideCharacter(code)) {
      width += 2
    } else {
      width += 1
    }
  }

  return width
}

